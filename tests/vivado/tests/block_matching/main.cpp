void loadImage(indata_t *picInput, const char *fileName, int PIC_WIDTH, int PIC_HEIGHT) {
  std::ifstream file (fileName);
  std::string s;
  int width;
  int height;
  int maxVal;
  if (file.is_open()) {
    file >> s;
    if (s != "P6") {
      std::cout << "wrong file format!" << std::endl;
    }
    file >> s;
    if (s == "#") {
      // catch comment line from gimp
      getline(file, s);
    } 
    file >> s;
    std::istringstream(s) >> width;
    file >> s;
    std::istringstream(s) >> height;
    file >> s;
    std::istringstream(s) >> maxVal;
    if (width != PIC_WIDTH || height != PIC_HEIGHT) {
      file.close();
      std::cout << "picture attributes differ from config file" << std::endl;
      exit(EXIT_FAILURE);
    }
    //ignore \n or whitepace
    file.get();
    for (int y = 0; y < PIC_HEIGHT; y++) {
      for (int x = 0; x < PIC_WIDTH; x++) {
#ifdef GRAYSCALE
        picInput[y * PIC_WIDTH + x] = file.get();
        file.get();
        file.get();
#else
        picInput[y * PIC_WIDTH + x].x = file.get();
        picInput[y * PIC_WIDTH + x].y = file.get();
        picInput[y * PIC_WIDTH + x].z = file.get();
        picInput[y * PIC_WIDTH + x].w = 0;
#endif
      }
    }
    file.close();
  } else {
    std::cout << "unable to open file" << std::endl;
  }
}


void writeOutputPic(outdata_t *picOutput, int PIC_WIDTH, int PIC_HEIGHT) {
  std::ofstream outfile("output.ppm");
  if (!outfile) {
    std::cout << "cannot open output file" << std::endl;
    exit(EXIT_FAILURE);
  }
  outfile << "P6 " << PIC_WIDTH << " " << PIC_HEIGHT << " 255\n";
  //outfile << "P6 " << PIC_WIDTH << " " << PIC_HEIGHT << " 512\n";
  for (int y = 0; y < PIC_HEIGHT; y++) {
    for (int x = 0; x < PIC_WIDTH; x++) {
#ifdef SIMPLE
      if (picOutput[y * PIC_WIDTH + x] > 255) {
        outfile << (uchar) 255;
        outfile << (uchar) 255;
        outfile << (uchar) 255;
      } else {
        outfile << (uchar) picOutput[y * PIC_WIDTH + x]; 
        outfile << (uchar) picOutput[y * PIC_WIDTH + x]; 
        outfile << (uchar) picOutput[y * PIC_WIDTH + x]; 
      }
#else
      if (picOutput[y * PIC_WIDTH + x].x > 255) {
        outfile << (uchar) 255;
      } else {
        outfile << (uchar) picOutput[y * PIC_WIDTH + x].x; 
      }
      if (picOutput[y * PIC_WIDTH + x].y > 255) {
        outfile << (uchar) 255;
      } else {
        outfile << (uchar) picOutput[y * PIC_WIDTH + x].y; 
      }
      if (picOutput[y * PIC_WIDTH + x].z > 255) {
        outfile << (uchar) 255;
      } else {
        outfile << (uchar) picOutput[y * PIC_WIDTH + x].z; 
      }
#endif
    }
  }
  if (!outfile.good()) {
    std::cout << "cannot write to output file" << std::endl;
    outfile.close();
    exit(EXIT_FAILURE);
  }
  outfile.close();
}



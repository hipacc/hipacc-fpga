class SAD : public Kernel<indata_t> {
private:
  Accessor<indata_t> &input1;
  Accessor<indata_t> &input2;
  Domain &idom;
  Domain &odom;

public:
  SAD(IterationSpace<outdata_t> &it, Accessor<indata_t> &in1,
      Accessor<indata_t> &in2, Domain &idom, Domain &odom)
      : Kernel(it), input1(in1), input2(in2), idom(idom), odom(odom) {
    add_accessor(&input1);
    add_accessor(&input2);
  }

  void kernel() {
    int pos = 0;
    int min = INT_MAX;

    iterate(odom, [&] () {
      int val = 0;
      iterate(idom, [&] () {
#ifdef GRAYSCALE
        int sum = input1(idom)
                 - input2(idom.x()+odom.x()+(DISPARITY/2), idom.getY());
        val += abs(sum);
#else
        int4 sum = convert_int4(input1(idom))
                 - convert_int4(input2(idom.x()+odom.x()+(DISPARITY/2), idom.getY()));
        val += abs(sum.x) + abs(sum.y) + abs(sum.z) + abs(sum.w);
#endif
      });

      if (val < min) {
        min = val;
        pos = odom.x()+(DISPARITY/2);
      }
    });

    uchar val = pos * 255 / (DISPARITY-1);
#ifdef SIMPLE
    output() = val;
#else
    uchar4 val4;
    val4.x = val4.y = val4.z = val4.w = val;
    output() = convert_uchar4(val4);
#endif
  }
};

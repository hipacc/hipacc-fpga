class SignatureKernel : public Kernel<sig_t> {
    private:
        Accessor<uchar> &input;
        Domain &dom;

    public:
        SignatureKernel(IterationSpace<sig_t> &iter, Accessor<uchar> &input,
                        Domain &dom) :
            Kernel(iter),
            input(input),
            dom(dom)
        { add_accessor(&input); }

        void kernel() {
            // Census Transformation
            uchar z = input();
            sig_t c = 0;
            uchar i = 0;
            iterate(dom, [&] () {
              c |= input(dom) > z ? 1 << i : 0;
              ++i;
            });

            output() = c;
        }
};

class CompareKernel : public Kernel<uchar> {
    private:
        Accessor<sig_t> &sig1, &sig2;
        Domain &dom;
        Domain &cdom;

    public:
        CompareKernel(IterationSpace<uchar> &iter, Accessor<sig_t> &sig1,
                      Accessor<sig_t> &sig2, Domain &dom, Domain &cdom)
            : Kernel(iter), sig1(sig1), sig2(sig2), dom(dom), cdom(cdom) {
            add_accessor(&sig1);
            add_accessor(&sig2);
        }

        void kernel() {
            int pos = -(DISPARITY/2);
            uchar min = 255;
            sig_t reference = sig1(pos,0);

            iterate(dom, [&] () {
                sig_t ref = reference ^ sig2(dom);
                uchar dist = 0;

                iterate(cdom, [&] () {
                  if ((ref >> (cdom.x()+(CENSUS_BITS/2))) & 1) {
                    ++dist;
                  }
                });

                // Alternative: Brian Kernighan algorithm, performs badly
                //iterate(cdom, [&] () {
                //  if (!ref) break_iterate();
                //  ++dist;
                //  ref &= ref - 1;
                //});

                if (dist < min) {
                  pos = dom.x();
                  min = dist;
                }
            });
            
            pos += DISPARITY/2;

            output() = pos * 255 / (DISPARITY - 1);
        }
};
